package com.sumridge.smart.bean;

import java.util.List;
import java.util.Map;

/**
 * Created by liu on 16/4/26.
 */
public class AccountAggBean {
    private String id;
    private List<Map<String,String>> accounts;

    public int getSize(){
        return accounts==null?0:accounts.size();
    }
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public List<Map<String, String>> getAccounts() {
        return accounts;
    }

    public void setAccounts(List<Map<String, String>> accounts) {
        this.accounts = accounts;
    }
}
