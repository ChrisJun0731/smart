package com.sumridge.smart.entity;

import org.springframework.data.annotation.Id;

import java.util.Date;
import java.util.Set;

/**
 * Created by liu on 16/4/15.
 */
public class BoardFileInfo {
    @Id
    private String id;
    private String fileType;
    private String fileName;
    private Long fileSize;
    private String creator;
    private Date createDate;
    private Date updateDate;
    private String status;
    private String parent;
    private String fileId;
    private Set<String> shareSet;
    private String boardId;
    private FileViewer fileViewer;
    private Set<String> pathSet;
    private Boolean delete;

    public Boolean getDelete() {
        return delete;
    }

    public void setDelete(Boolean delete) {
        this.delete = delete;
    }

    public Set<String> getPathSet() {
        return pathSet;
    }

    public void setPathSet(Set<String> pathSet) {
        this.pathSet = pathSet;
    }

    public FileViewer getFileViewer() {
        return fileViewer;
    }

    public void setFileViewer(FileViewer fileViewer) {
        this.fileViewer = fileViewer;
    }

    public String getBoardId() {
        return boardId;
    }

    public void setBoardId(String boardId) {
        this.boardId = boardId;
    }

    public String getFileId() {
        return fileId;
    }

    public void setFileId(String fileId) {
        this.fileId = fileId;
    }

    public Set<String> getShareSet() {
        return shareSet;
    }

    public void setShareSet(Set<String> shareSet) {
        this.shareSet = shareSet;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFileType() {
        return fileType;
    }

    public void setFileType(String fileType) {
        this.fileType = fileType;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public Long getFileSize() {
        return fileSize;
    }

    public void setFileSize(Long fileSize) {
        this.fileSize = fileSize;
    }

    public String getCreator() {
        return creator;
    }

    public void setCreator(String creator) {
        this.creator = creator;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    public Date getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(Date updateDate) {
        this.updateDate = updateDate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getParent() {
        return parent;
    }

    public void setParent(String parent) {
        this.parent = parent;
    }
}
