public class StringUtil {

    public static String getInitChar(String str) {
        return str== null || str.equals("")?null:str.substring(0,1).toUpperCase();
    }

    public static String toString(Object obj) {
        if(obj == null) {
            return null;
        } else {
           return obj.toString();
        }
    }
}
